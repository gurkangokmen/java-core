
public class JavaMath
{



    public static void main(String[] args)
    {
        System.out.println(args);
        // Math.max(x,y)
        System.out.println(Math.max(5, 10));                                 //Output:                     10

        // Math.min(x,y)
        System.out.println(Math.min(5, 10));                                 //Output:                     5

        // Math.sqrt(x)
        System.out.println(Math.sqrt(64));                                   //Output:                     8.0

        // Math.abs(x)
        System.out.println(Math.abs(-4.7));                                  //Output:                     4.7

        // Math.random()
        System.out.println(Math.random());                                   //Output:                     0.19938808182941814

        // Math.PI
        System.out.println(Math.PI);                                         //Output:                     3.141592653589793

    }



}
