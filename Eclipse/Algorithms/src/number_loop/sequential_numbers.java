package number_loop;

public class sequential_numbers {

	public static void main(String[] args) {
		// Sequential numbers: 1 to 10
		for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
		
		// Sequential odd numbers: 1 to 10
		for (int i = 1; i <= 10; i += 2) {
            System.out.println(i);
        }
		
		// Sequential even numbers: 1 to 10
		for (int i = 2; i <= 10; i += 2) {
            System.out.println(i);
        }
		
		

	}

}
